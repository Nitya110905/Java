package My_Package;
//Primitive: byte, short, int, long, float, double, char, boolean
//Non-primitive: array, string, class

public class P003_Datatype {

		public static void main(String[] args) {
			byte b = 127;//1
			short s = 12;//2
			int i = 12;//4
			long l = 123565;//8
			float f = 3.14f;//4
			double d = 3.14;//8
			char c = 'a';//1
			boolean bool = true;//1
			System.out.println(bool);
		}

}
